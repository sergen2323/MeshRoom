let LANG = localStorage.getItem("mr-lang") ||
  ((navigator.language || "ru").toLowerCase().startsWith("ru") ? "ru" : "en");

function t(key) {
  return (DICT[LANG] && DICT[LANG][key]) || DICT.ru[key] || key;
}

function toggleLang() {
  LANG = LANG === "ru" ? "en" : "ru";
  localStorage.setItem("mr-lang", LANG);
}
